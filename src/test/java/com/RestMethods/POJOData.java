package com.RestMethods;

public class POJOData
{
//Encapsulation=data+function
	
	private String name;
	private String job;
	
	
	//getters and setters
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getJob() {
		return job;
	}
	public void setJob(String job) {
		this.job = job;
	}
	
}
